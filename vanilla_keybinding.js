document.onkeydown = keyCheck;

function keyCheck(e)
{
  var keycode = window.event.keyCode;

  switch (keycode)
  {
    case 37:  // left arrow 
		// action
	break;

    case 38:  // up arrow 
		// action
	break;

    case 39:  // right arrow 
		// action
	break;

    case 40:  // down arrow 
		// action
	break;
  }
}
